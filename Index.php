                <h3>Trama</h3>
                <p>La historia central se desarrolla en el año 2022, cuando un videojuego de rol multijugador masivo en
                    línea de realidad virtual —VRMMORPG, por sus siglas en inglés (Virtual Reality Massive Multiplayer
                    Online Role Playing Game)— llamado «Sword Art Online» acaba de ser lanzado. Allí, los jugadores
                    pueden entrar a un mundo denominado Aincrad y controlar sus avatares tal como lo harían con sus
                    cuerpos reales por medio del NerveGear, un casco de realidad virtual capaz de estimular sus cinco
                    sentidos a través de sus cerebros. Sin embargo, los jugadores se dan cuenta de que no pueden salir
                    del videojuego; poco tiempo después, el creador de SAO, Akihiko Kayaba, aparece y les informa que si
                    mueren en el mundo virtual también lo harán en el mundo real, así como les revela que para poder
                    salir del juego primero deben superar los cien pisos del juego. A medida que los jugadores tratan
                    de hacer frente a su situación, dos de ellos Kazuto Kirigaya y Asuna Yuuki, están decididos a ganar
                    y ser libres.
                </p>
