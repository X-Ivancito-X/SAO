                <h1>SAO: Sword Art Online</h1>
                <p>Sword Art Online (ソードアート・オンライン Sōdo Āto Onrain?), también conocida como SAO, es una serie de novela
                    ligera japonesa escrita por Reki Kawahara e ilustradas por Abec. A partir de estas, se han creado
                    diversas adaptaciones, tanto de manga como de anime. Se comenzó a publicar en abril de 2009 por la
                    editorial ASCII Media Works, y se compone hasta junio de 2024 por 28 volúmenes.

                    Por su parte, la adaptación al anime fue producida por el estudio A-1 Pictures, dirigida por
                    Tomohiko Itō y emitida en Japón por diversas cadenas televisivas desde el 7 de julio de 2012 hasta
                    que finalizó el 22 de diciembre del mismo año con su vigésimo quinto episodio. Su segunda
                    temporada se emitió entre julio y diciembre de 2014, mientras que su tercera temporada fue emitida
                    desde octubre de 2018 hasta septiembre de 2020.
                </p>
