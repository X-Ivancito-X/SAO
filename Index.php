            <div class="infoSAO Glass">
                <h3>Produccion</h3>
                <p>Reki Kawahara escribió el primer volumen en 2001 como una participación en el concurso para el Premio
                    de Novela de Juegos Dengeki de ASCII Media Works 2002 (電撃ゲーム小説大賞, Dengeki Game Shōsetsu Taishō,
                    ahora Premio de Novela Denge). Con el tiempo, añadió otros tres arcos principales y varios cuentos
                    que, al igual que el primer arco "Aincrad", se adaptaron más tarde a las novelas ligeras. En 2008,
                    participó de nuevo en el concurso escribiendo Accel World, esta vez ganando el Gran Premio. Aparte
                    de Accel World, se le pidió que publicara su trabajo anterior, Sword Art Online, por ASCII Media
                    Works. Estuvo de acuerdo y retiró sus versiones de novelas web.
                </p>
            </div>

            <div class="infoSAO Glass">
                <h1>Contenido de la obra</h1>
                <h4>Novelas ligeras</h4>
                <p>En su momento, Reki Kawahara escribió el primer volumen de la novela en el 2002 para poder entrar en
                    la competencia de los premios Dengeki Novel Prize de la editorial ASCII Media Works, pero se abstuvo
                    de presentarlo dado que había excedido el límite de páginas permitido. No obstante, Kawahara optó
                    por publicarlo en la web bajo su seudónimo Fumio Kunori. Posteriormente, el autor agregó a su
                    página tres volúmenes nuevos y múltiples cuentos.
                </p>
            </div>

            <div class="infoSAO Glass">
                <h4>Anime</h4>
                <p>La adaptación al anime de Sword Art Online fue anunciada en el Dengeki Bunko Autumn Festival de 2011,
                    junto a la adaptación de Accel World, otra novela ligera del mismo autor.

                    El anime fue dirigido por Tomohiko Itō, producido por el estudio de animación A-1 Pictures, y su
                    banda sonora fue compuesta por Yuki Kajiura. Se emitió por primera vez en Japón en las cadenas
                    televisivas Tokyo MX, tvk, TVS, TV Aichi, RKB, HBC y MBS el 7 de julio de 2012; mientras que las
                    cadenas AT-X, Chiba TV y BS11 lo transmitieron en días posteriores. El anime finalizó el 22 de
                    diciembre de 2012 con un total de veinticinco episodios de unos veinticuatro minutos cada uno.
                </p>
            </div>

